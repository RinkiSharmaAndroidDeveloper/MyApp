package tecaa.in.com.myapplication.tecsaa.ui.homeWorkTeacher;

public class Data {
    String name;
    int id;

    public Data(String name, int id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
